namespace Hx.Abp.Attachment.Domain.Shared
{
    /// <summary>
    /// 模板角色枚举 - 标识模板在层级结构中的角色
    /// 主要用于前端树状展示和动态分类树创建判断
    /// </summary>
    public enum TemplateRole
    {
        /// <summary>
        /// 根模板 - 可以作为根节点创建动态分类树
        /// 特点：顶级模板，没有父模板，前端可以基于此创建树状结构
        /// 应用场景：知识库根目录、档案管理根分类、项目根节点等
        /// </summary>
        Root = 1,

        /// <summary>
        /// 导航模板 - 仅用于导航，不参与动态分类树创建
        /// 特点：纯导航作用，前端展示但不作为树节点
        /// 应用场景：侧边栏导航、面包屑导航、菜单项等
        /// </summary>
        Navigation = 2,

        /// <summary>
        /// 普通模板 - 既不是根模板也不是导航模板的普通模板
        /// 特点：可以作为子模板使用，但不能作为根节点创建动态分类树
        /// 应用场景：子分类模板、中间层模板等
        /// </summary>
        Normal = 3
    }
}
