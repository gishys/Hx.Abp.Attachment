namespace Hx.Abp.Attachment.Domain.Shared
{
    /// <summary>
    /// 模板角色枚举 - 标识模板在层级结构中的角色
    /// 主要用于前端树状展示和动态分类树创建判断
    /// </summary>
    public enum TemplateRole
    {
        /// <summary>
        /// 根模板 - 可以作为根节点创建动态分类树
        /// 特点：顶级模板，没有父模板，前端可以基于此创建树状结构
        /// 应用场景：知识库根目录、档案管理根分类、项目根节点等
        /// </summary>
        Root = 1,

        /// <summary>
        /// 导航模板 - 仅用于导航，不参与动态分类树创建
        /// 特点：纯导航作用，前端展示但不作为树节点
        /// 应用场景：侧边栏导航、面包屑导航、菜单项等
        /// </summary>
        Navigation = 2,

        /// <summary>
        /// 分支节点 - 可以有子节点，但不能直接上传文件
        /// 特点：中间层节点，用于组织分类结构，前端可以在此节点下创建子节点
        /// 应用场景：分类文件夹、组织架构中间层、知识库分类等
        /// </summary>
        Branch = 3,

        /// <summary>
        /// 叶子节点 - 不能有子节点，但可以直接上传文件
        /// 特点：终端节点，用于存放具体内容，前端可以在此节点上传文件
        /// 应用场景：具体文档分类、文件存储目录、内容分类等
        /// </summary>
        Leaf = 4
    }
}
